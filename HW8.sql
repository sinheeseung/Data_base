
select EMPNO 사원번호, ENAME 이름, JOB 직무, DEPTNO 부서번호
FROM EMP
WHERE DEPTNO = 10
UNION
select EMPNO 사원번호, ENAME 이름, JOB 직무, DEPTNO 부서번호
FROM EMP
WHERE DEPTNO = 20
ORDER BY 4;

